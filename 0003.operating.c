#include <stdio.h>

/**
 * 自增自减操作
 * @return
 */
int main() {
    int x = 0;
    printf("x++ = %d\n", x++);
    printf("++x = %d\n", ++x);
    printf("x-- = %d\n", x--);
    printf("--x = %d\n", --x);
    printf("x -= 1 = %d\n", x -= 1);
    printf("x += 1 = %d\n", x += 1);
    return 0;
}

