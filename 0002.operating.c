#include <stdio.h>

/**
 * 运算符
 * +
 * -
 * *
 * /
 * @return
 */
int main() {
    int x, y;
    x = 10;
    y = -3;
    printf("x + y = %d\n", x + y);
    printf("x - y = %d\n", x - y);
    printf("x * y = %d\n", x * y);
    printf("x / y = %d\n", x / y);
    printf("x %% y = %d\n", x % y);
    return 0;
}